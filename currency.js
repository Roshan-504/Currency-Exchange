let URL = "https://v6.exchangerate-api.com/v6/ce00200d8907207a8571143f/latest/";
let submit = document.getElementById("submit");
let From = document.getElementById("From");
let To = document.getElementById("To");
let msg = document.getElementsByClassName("msg");


let select = document.querySelectorAll("select");
for(let se of select){
    for(currency in countryList){
        let newOption = document.createElement("option");
        newOption.innerHTML = currency;
        newOption.value = currency;
        if(se.name==="From" && currency==="USD"){
            newOption.selected = "selected"
        }
        if(se.name==="To" && currency==="INR"){
            newOption.selected = "selected"
        }

        se.append(newOption)
    }
    se.addEventListener("change",(evt) => {
        changeFlag(evt.target)
    })
}

function changeFlag(element){
    let changedCurrency = element.value
    let fromImage = document.getElementById("fromImage");
    let toImage = document.getElementById("toImage");
    if (element === From){
        fromImage.src = `https://flagsapi.com/${countryList[changedCurrency]}/flat/64.png`;
    }
    if (element === To){
        toImage.src = `https://flagsapi.com/${countryList[changedCurrency]}/flat/64.png`;
    }
}

submit.addEventListener("click" ,async (evt) => {
    evt.preventDefault();
    let input = document.getElementById("input");
    if(input.value<=0){
        input.value = 1
    }
    console.log(input.value)
    let baseCurrency = URL+From.value;
    let response = await fetch(baseCurrency)
    let data = await response.json()
    let rates = data.conversion_rates
    let amount = input.value*rates[To.value]
    msg[0].innerHTML = input.value+ " " + From.value +" = " + amount + " " + To.value
})








